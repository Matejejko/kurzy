### using ** ###
print('**')
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)
print()

### using * ###
print('*')
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)
print()

### using / ###
print('/')
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)
print()

### using // ###
print('//')
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)
print()

### - numbers ###
print('- numbers')
print(-6 // 4)
print(6. // -4)
print()

### % ###
print('%')
print(14 % 4)
print()

### more stuff ###
print('more stuff')
print(2 * 3 % 5)
print()
### using () ###
print('using ()')
print((5 * ((25 % 13) + 100) / (2 * 13)) // 2)